def _render_custom_blocks(parts: list[str], config: dict[str, Any]) -> None:
    custom = config.get("custom_blocks", [])
    if custom:
        _w(parts, "# === Project-Specific Blocks ===")
        _w(parts, "")
        for block in custom:
            _w(parts, block)
        _w(parts, "")


