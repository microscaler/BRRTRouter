def _render_helpers(parts: list[str], config: dict[str, Any]) -> None:
    suite = config["suite_name"]
    pname = config["project_name"]
    cli_name = config.get("cli_name", "brrtrouter")
    wrapped = cli_name != "brrtrouter"
    upper = suite.upper().replace("-", "_")
    gen_bin = f"{pname}_bin" if wrapped else "brrtrouter_gen_bin"

    _w(parts, "# === Helper functions ===")
    _w(parts, "")

    # get_service_port
    _w(parts, "def get_service_port(name):")
    _w(parts, f"    if name in {upper}_PORTS:")
    _w(parts, f"        return {upper}_PORTS[name]")
    _w(parts, "    return '8080'")
    _w(parts, "")

    # get_package_name
    _w(parts, "def get_package_name(name):")
    if wrapped:
        _w(parts, f"    fallback_pkg = 'rerp_{suite}_' % ('{suite}') + name.replace('-', '_')")
    else:
        _w(
            parts,
            f"    fallback_pkg = '{pname}_{suite}_' % (pname, '{pname}') + name.replace('-', '_')",
        )
    if wrapped:
        _w(parts, f"    manifest = 'microservices/{suite}/%s/impl/Cargo.toml' % name")
    else:
        _w(parts, "    manifest = 'microservices/%s/impl/Cargo.toml' % name")
    _w(
        parts,
        f"    if str(local('test -e {_BSDQ}%s{_BSDQ} && echo yes || true' % manifest, quiet=True)).strip() != 'yes':",
    )
    _w(parts, "        return fallback")
    _w(parts, "")
    _w(parts, "    content = str(read_file(manifest))")
    _w(parts, "    in_package = False")
    _w(parts, f"    for raw in content.split('{_BSBS}n'):")
    _w(parts, "        stripped = raw.strip()")
    _w(parts, "        if stripped == '[package]':")
    _w(parts, "            in_package = True")
    _w(parts, "            continue")
    _w(parts, "        if stripped.startswith('[') and stripped != '[package]':")
    _w(parts, "            in_package = False")
    _w(parts, "            continue")
    _w(parts, '        if in_package and stripped.startswith("name = "):')
    _w(
        parts,
        f"            return stripped.split('=', 1)[1].strip().strip('{_DQ}').strip('{chr(39)}')",
    )
    _w(parts, "    return fallback")
    _w(parts, "")

    # get_binary_name (PW only)
    if not wrapped:
        _w(parts, "def get_binary_name(name):")
        _w(parts, "    if name in BINARY_NAMES:")
        _w(parts, "        return BINARY_NAMES[name]")
        _w(parts, "    return name.replace('-', '_') + '_impl'")
        _w(parts, "")

    # create_microservice_lint
    _w(parts, "def create_microservice_lint(name, spec_file):")
    _w(parts, "    local_resource(")
    _w(parts, "        '%s-lint' % name,")
    _w(parts, "        cmd='''")
    lint_lines = [
        "            set -e",
        '            echo "🔍 Linting %s OpenAPI spec..." % name',
        f"            {gen_bin} lint {_BSDQ}",
        f"                --spec ./openapi/{suite}/%s {_BSDQ} % (spec_file, spec_file)",
        "                --fail-on-error || \\",
        "            cargo run --manifest-path "
        + "brrtrouter_root/Cargo.toml --bin brrtrouter-gen -- \\",
        "                lint \\",
        f"                --spec ./openapi/{suite}/%s {_BSDQ} % (spec_file, spec_file)",
        "                --fail-on-error",
        '            echo "✅ %s OpenAPI spec linting passed" % name',
    ]
    _w(parts, "\n".join(lint_lines))
    _w(parts, "        '''")
    _w(parts, "        deps=['./openapi/%s/%s' % (suite, spec_file)],")
    _w(parts, "        resource_deps=[],")
    _w(parts, f"        labels=['{pname}_' + name],")
    _w(parts, "        allow_parallel=True,")
    _w(parts, "    )")
    _w(parts, "")

    # create_microservice_gen
    if wrapped:
        gen_cmd = f"{pname} gen suite %s --service %s" % (suite, "name")
    else:
        gen_cmd = (
            f"{cli_name} client gen suite %s --service %s --openapi-dir microservices/openapi"
            % (suite, "name")
        )
    _w(parts, "def create_microservice_gen(name, spec_file, output_dir):")
    _w(parts, "    local_resource(")
    _w(parts, "        '%s-service-gen' % name,")
    _w(parts, f"        cmd='{gen_cmd}'")
    _w(parts, "        deps=[")
    _w(parts, "            './openapi/%s/%s' % (suite, spec_file),")
    _w(parts, "            'tooling/pyproject.toml',")
    _w(parts, "        ],")
    _w(parts, "        ignore=[")
    for section in ["gen/src", "gen/doc", "impl/config", "gen/static_site"]:
        _w(parts, f"            './microservices/{suite}/%s/{section}' % name,")
    _w(parts, "        ],")
    _w(parts, "        resource_deps=['%s-lint' % name],")
    _w(parts, f"        labels=['{pname}_' + name],")
    _w(parts, "        allow_parallel=True,")
    _w(parts, "    )")
    _w(parts, "")

    # create_microservice_build_resource
    if wrapped:
        build_cmd = f"{pname} build microservice %s" % "name"
    else:
        build_cmd = f"{cli_name} client build microservice %s" % "name"
    _w(parts, "def create_microservice_build_resource(name):")
    _w(parts, "    local_resource(")
    _w(parts, "        'build-%s' % name,")
    _w(parts, f"        cmd='{build_cmd}'")
    _w(parts, "        deps=[")
    for section in ["gen/Cargo.toml", "impl/Cargo.toml", "gen/src", "impl/src"]:
        _w(parts, f"            './microservices/{suite}/%s/{section}' % name,")
    _w(parts, "            'tooling/pyproject.toml',")
    _w(parts, "        ],")
    _w(parts, "        ignore=[")
    _w(parts, "            './microservices/target',")
    _w(parts, "            './build_artifacts',")
    _w(parts, "        ],")
    _w(parts, f"        resource_deps=['%s-{suite}-all-gens']" % pname)
    _w(parts, f"        labels=['{pname}_' + name],")
    _w(parts, "        allow_parallel=True,")
    _w(parts, "    )")
    _w(parts, "")

    # create_microservice_deployment
    prefix = (
        config["docker_image_prefix"].split("%")[0]
        if "%" in config["docker_image_prefix"]
        else config["docker_image_prefix"]
    )
    if wrapped:
        copy_cmd = f"{pname} docker copy-binary %s %s %s" % (
            "target_path",
            "artifact_path",
            "binary_name",
        )
        docker_cmd = f"{pname} docker build-image-simple %s %s %s --service %s" % (
            "image_name",
            "hash_path",
            "artifact_path",
            "name",
        )
    else:
        copy_cmd = f"{cli_name} docker copy-binary %s %s %s" % (
            "target_path",
            "artifact_path",
            "binary_name",
        )
        docker_cmd = f"{cli_name} docker build-image-simple %s %s %s --service %s" % (
            "image_name",
            "hash_path",
            "artifact_path",
            "name",
        )

    _w(parts, "def create_microservice_deployment(name):")
    _w(parts, "    package_name = get_package_name(name)")
    if wrapped:
        _w(parts, "    binary_name = name.replace('-', '_')")
    else:
        _w(parts, "    binary_name = get_binary_name(name)")
    _w(
        parts,
        f"    target_path = 'microservices/target/{config['target_rust_triple']}/debug/%s' % package_name",
    )
    _w(parts, "    artifact_path = 'build_artifacts/%s' % binary_name")
    _w(parts, f"    image_name = '{prefix}-%s' % name")
    _w(parts, "    port = get_service_port(name)")
    _w(parts, "")
    _w(parts, "    hash_path = 'build_artifacts/%s.sha256' % binary_name")
    _w(parts, "    local_resource(")
    _w(parts, "        'copy-%s' % name,")
    _w(parts, f"        cmd='{copy_cmd}'")
    _w(parts, "        deps=[target_path, 'tooling/pyproject.toml'],")
    _w(parts, "        resource_deps=['build-%s' % name],")
    _w(parts, f"        labels=['{pname}_' + name],")
    _w(parts, "        allow_parallel=True,")
    _w(parts, "    )")
    _w(parts, "")
    _w(parts, "    local_resource(")
    _w(parts, "        'docker-%s' % name,")
    _w(parts, f"        cmd='{docker_cmd}'")
    _w(
        parts,
        "        deps=[hash_path, artifact_path, 'docker/microservices/Dockerfile.template', 'tooling/pyproject.toml'],",
    )
    _w(parts, "        resource_deps=['copy-%s' % name],")
    _w(parts, f"        labels=['{pname}_' + name],")
    _w(parts, "        allow_parallel=False,")
    _w(parts, "    )")
    _w(parts, "")

    helm_values = "'{}/values/{}.yaml' % ('{}', name)".format(
        config["helm_chart_path"], config["helm_chart_path"], "name"
    )
    _w(
        parts,
        "    k8s_yaml(helm('{}' % '{}', name=name, namespace='{}', values=[{}]))".format(
            config["helm_chart_path"], config["helm_chart_path"], pname, helm_values
        ),
    )
    _w(parts, "")
    _w(parts, "    k8s_resource(")
    _w(parts, "        name,")
    _w(parts, "        port_forwards=['%s:%s' % (port, port)],")
    _w(parts, "        resource_deps=['{}-database-env', 'docker-{}'],".format(pname, "name"))
    _w(parts, f"        labels=['{pname}_' + name],")
    _w(parts, "        auto_init=True,")
    _w(parts, "    )")
    _w(parts, "")


