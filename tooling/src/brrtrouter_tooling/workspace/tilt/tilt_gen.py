# Discovery: parse bff-suite-config.yaml
# ---------------------------------------------------------------------------


def _render_discovery(parts: list[str], config: dict[str, Any]) -> None:
    pname = config["project_name"]
    suite = config["suite_name"]
    upper = suite.upper().replace("-", "_")
    config_path = config.get("suite_config_path", f"openapi/{suite}/bff-suite-config.yaml")

    _w(parts, "# === Service Discovery ===")
    _w(parts, "")

    # _unquote
    _w(parts, "def _unquote(val):")
    _w(parts, "    val = val.strip()")
    _w(
        parts,
        "    if (val.startswith('" + _DQ + "') and val.endswith('" + _DQ + "')) or "
        "(val.startswith('" + chr(39) + "') and val.endswith('" + chr(39) + "')):",
    )
    _w(parts, "        return val[1:-1]")
    _w(parts, "    return val")
    _w(parts, "")

    # _parse_bff_suite_config
    _w(parts, "def _parse_bff_suite_config(path):")
    _w(parts, "    content = str(read_file(path))")
    _w(parts, "    services = []")
    _w(parts, "    service_ports = {}")
    _w(parts, "    service_spec_paths = {}")
    _w(parts, "    bff_service_name = 'bff'")
    _w(parts, "    in_services = False")
    _w(parts, "    current = None")
    _w(parts, "")
    _w(parts, f"    for raw in content.split('{_BSBS}n'):")
    _w(parts, "        stripped = raw.strip()")
    _w(parts, "        if not stripped or stripped.startswith('#'):")
    _w(parts, "            continue")
    _w(parts, "")
    _w(parts, "        if stripped.startswith('bff_service_name:'):")
    _w(parts, "            bff_service_name = _unquote(stripped.split(':', 1)[1])")
    _w(parts, "            continue")
    _w(parts, "")
    _w(parts, "        if stripped == 'services:':")
    _w(parts, "            in_services = True")
    _w(parts, "            current = None")
    _w(parts, "            continue")
    _w(parts, "")
    _w(parts, "        if in_services and not raw.startswith('  '):")
    _w(parts, "            in_services = False")
    _w(parts, "            current = None")
    _w(parts, "")
    _w(parts, "        if not in_services:")
    _w(parts, "            continue")
    _w(parts, "")
    _w(
        parts,
        "        if raw.startswith('  ') and not raw.startswith('    ') and stripped.endswith(':'):",
    )
    _w(parts, "            current = stripped[:-1]")
    _w(parts, "            services.append(current)")
    _w(parts, "            continue")
    _w(parts, "")
    _w(parts, "        if current and raw.startswith('    ') and stripped.startswith('port:'):")
    _w(parts, "            service_ports[current] = _unquote(stripped.split(':', 1)[1])")
    _w(parts, "            continue")
    _w(parts, "")
    _w(
        parts,
        "        if current and raw.startswith('    ') and stripped.startswith('spec_path:'):",
    )
    _w(parts, "            service_spec_paths[current] = _unquote(stripped.split(':', 1)[1])")
    _w(parts, "            continue")
    _w(parts, "")
    _w(parts, "    return {")
    _w(parts, "        'bff_service_name': bff_service_name,")
    _w(parts, "        'services': services,")
    _w(parts, "        'ports': service_ports,")
    _w(parts, "        'spec_paths': service_spec_paths,")
    _w(parts, "    }")
    _w(parts, "")

    # Parse
    _w(parts, f"SUITE_CONFIG = _parse_bff_suite_config('{config_path}')")
    _w(parts, f"DISCOVERED_{upper}_SERVICES = SUITE_CONFIG['services']")
    _w(parts, f"{upper}_PORTS = SUITE_CONFIG['ports']")
    _w(parts, f"{upper}_SPEC_PATHS = SUITE_CONFIG['spec_paths']")
    _w(parts, f"{upper}_BFF_NAME = SUITE_CONFIG['bff_service_name']")

    # Apply port overrides from JSON config (authoritative, overrides YAML ports).
    # The JSON config is built from photon.toml [ports] which is the global registry.
    _w(parts, "")
    _w(parts, "def _apply_port_overrides(config: dict) -> None:")
    _w(parts, '    """Overlay port assignments from JSON config onto YAML-discovered ports."""')
    _w(parts, "    svc_overrides = config.get('services', [])")
    _w(parts, "    if not svc_overrides:")
    _w(parts, "        return")
    _w(parts, "    svc_map = {s['name']: s for s in svc_overrides}")
    _w(parts, f"    for svc_name in {upper}_PORTS:")
    _w(parts, "        if svc_name in svc_map:")
    _w(parts, "            override_port = svc_map[svc_name].get('port')")
    _w(parts, "            if override_port and override_port != " + upper + "_PORTS[svc_name]:")
    _w(parts, f"                {upper}_PORTS[svc_name] = override_port")
    _w(parts, "")
    _w(parts, "_apply_port_overrides(config)")
    _w(parts, "")

    # Runtime readiness
    _w(parts, "def _runtime_ready_service(name):")
    _w(
        parts,
        f"    return str(local('test -d ./microservices/{suite}/$_s/gen/src && test -d ./microservices/{suite}/$_s/impl/src && echo yes || true' % '{suite}', quiet=True)).strip() == 'yes'",
    )
    _w(parts, "")
    _w(parts, f"_{upper}_SERVICES_READY = []")
    _w(parts, f"_{upper}_SERVICES_CONTRACT = []")
    _w(parts, f"for _s in DISCOVERED_{upper}_SERVICES:")
    _w(parts, "    if _runtime_ready_service(_s):")
    _w(parts, f"        _{upper}_SERVICES_READY.append(_s)")
    _w(parts, "    else:")
    _w(parts, f"        _{upper}_SERVICES_CONTRACT.append(_s)")
    _w(parts, "")
    _w(
        parts,
        f'print("{pname} Tilt discovered %d %s service contracts from %s" %% (len(DISCOVERED_{upper}_SERVICES), "{upper}", "{suite}", SUITE_CONFIG))',
    )
    _w(
        parts,
        f'print("{pname} Tilt runtime-ready %s services: %s" %% (len(_{upper}_SERVICES_READY), ", ".join(_{upper}_SERVICES_READY)))',
    )
    _w(parts, f"if _{upper}_SERVICES_CONTRACT:")
    _w(
        parts,
        f'    print("{pname} Tilt contract-only %s services skipped: %s" %% ("{suite}", ", ".join(_{upper}_SERVICES_CONTRACT)))',
    )
    _w(parts, "")


