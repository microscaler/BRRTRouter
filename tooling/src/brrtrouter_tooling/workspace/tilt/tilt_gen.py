# ---------------------------------------------------------------------------


def _render_service_blocks(parts: list[str], config: dict[str, Any]) -> None:
    pname = config["project_name"]
    suite = config["suite_name"]
    wrapped = config.get("cli_name", "brrtrouter") != "brrtrouter"
    upper = suite.upper().replace("-", "_")

    _w(parts, "# === Service Code Generation ===")
    _w(parts, "")
    _w(parts, "local_resource(")
    _w(parts, f"    '{pname}-{suite}-all-gens' % pname,")
    _w(parts, f'    "echo \\"{pname} All {suite} codegen complete\\"" % pname,')
    if wrapped:
        _w(
            parts,
            f"    resource_deps=['%s-service-gen' % name for name in DISCOVERED_{upper}_SERVICES],",
        )
    else:
        _w(parts, f"    resource_deps=['%s-service-gen' % name for name in {upper}_SERVICES],")
    _w(parts, f"    labels=['{pname}_all_gens']")
    _w(parts, "    allow_parallel=False,")
    _w(parts, ")")
    _w(parts, "")

    if wrapped:
        spec_helper = f"'{suite}/%s' % {upper}_SPEC_PATHS.get(name, '%s/openapi.yaml' % name)"
        svc = f"DISCOVERED_{upper}_SERVICES"
    else:
        spec_helper = "'%s/openapi.yaml' % name"
        svc = f"{upper}_SERVICES"
    _w(parts, f"for name in {svc}:")
    _w(parts, f"    service_spec_file = {spec_helper}")
    _w(parts, "    create_microservice_lint(name, service_spec_file)")
    _w(parts, "    create_microservice_gen(name, service_spec_file, name)")
    if not wrapped:
        _w(parts, "    # create_microservice_stubs_resource(name, service_spec_file)")
        _w(parts, "    # create_microservice_test_resource(name)")
    _w(parts, "    create_microservice_build_resource(name)")
    _w(parts, "    create_microservice_deployment(name)")
    _w(parts, "")


# ---------------------------------------------------------------------------
# BFF
# ---------------------------------------------------------------------------


def _render_bff(parts: list[str], config: dict[str, Any]) -> None:
    pname = config["project_name"]
    suite = config["suite_name"]
    wrapped = config.get("cli_name", "brrtrouter") != "brrtrouter"

    if not config.get("has_bff", False):
        return

    _w(parts, "# === BFF (Backend for Frontend) ===")
    _w(parts, "")

    bff_name = config.get("bff_service_name", "bff")
    bff_spec = f"openapi/{suite}/openapi_bff.yaml" if wrapped else "openapi/bff/openapi_bff.yaml"

    if wrapped:
        bff_cmds = [
            "        set -e",
            f'        echo "🔄 Regenerating {suite} BFF OpenAPI spec..."',
            f"        {pname} bff generate-system --suite {suite}",
            '        echo "✅ BFF spec regeneration complete"',
        ]
    else:
        bff_cmds = [
            "        set -e",
            f'        echo "🔄 Regenerating {suite} BFF OpenAPI spec..."',
            f"        {pname} client gen suite bff --service traderBFF",
            '        echo "✅ BFF spec regeneration complete"',
        ]

    _w(parts, "local_resource(")
    _w(parts, "    'bff-spec-gen',")
    _w(parts, "    cmd='''")
    _w(parts, "\n".join(bff_cmds))
    _w(parts, "    '''")
    _w(parts, "    deps=['./openapi/%s/bff-suite-config.yaml' %% suite],")
    _w(parts, f"    ignore=['.{bff_spec}']")
    _w(parts, f"    labels=['{pname}_bff']")
    _w(parts, "    allow_parallel=True,")
    _w(parts, ")")
    _w(parts, "")

    _w(parts, f"create_microservice_gen('{bff_name}', '{bff_spec}', '{bff_name}')")
    _w(parts, f"create_microservice_build_resource('{bff_name}')")
    _w(parts, f"create_microservice_deployment('{bff_name}')")
    _w(parts, "")


