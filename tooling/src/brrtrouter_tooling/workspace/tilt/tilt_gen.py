"""Tiltfile generation for BRRTRouter-based projects.

Single pattern: parse bff-suite-config.yaml for service discovery, then render
the standard Tiltfile sections. Works for RERP, PriceWhisperer, Hauliage, etc.

Usage:
    from brrtrouter_tooling.workspace.tilt.tilt_gen import generate_tiltfile
    tiltfile = generate_tiltfile(config)
    Path("Tiltfile").write_text(tiltfile)
"""

from __future__ import annotations

from typing import Any

# Tiltfile uses %% for literal % at runtime.
# We build strings with f-strings/format() so Python's % operator never
# conflicts with Tiltfile's % operators.
_TILT_FMT = "%s"  # Placeholder we use in Tiltfile code (literal in output)


def generate_tiltfile(config: dict[str, Any]) -> str:
    parts: list[str] = []
    _render_header(parts, config)
    _render_discovery(parts, config)
    _render_helpers(parts, config)
    _render_service_blocks(parts, config)
    _render_bff(parts, config)
    _render_custom_blocks(parts, config)
    return "\n".join(parts) + "\n"


# ---------------------------------------------------------------------------
# String builders — avoid Python % operator to never conflict with Tiltfile %
# ---------------------------------------------------------------------------

_DQ = '"'  # double quote
_BS = "\\"  # backslash
_BSDQ = "\\" + _DQ  # \"
_BSBS = "\\\\"  # \\


def _w(parts: list[str], line: str) -> None:
    parts.append(line)


def _fmt(parts: list[str], template: str, *args: Any) -> None:
    """Format a Tiltfile line using positional .format().
    Tiltfile uses %% for literal % at runtime.
    This helper takes a template with {0}, {1}... and fills them in.
    The result is a Python string that, when written, contains the literal %%s etc.
    """
    parts.append(template.format(*args))


# ---------------------------------------------------------------------------
# Header: config, env vars, tooling install, k8s namespaces
# ---------------------------------------------------------------------------


def _render_header(parts: list[str], config: dict[str, Any]) -> None:
    pname = config["project_name"]
    cli_name = config.get("cli_name", "brrtrouter")
    brrt_rel = config.get("brrtrouter_rel_path", "../BRRTRouter")
    brrt_venv = config.get("brrtrouter_venv", "~/.local/share/brrtrouter/venv")
    brrt_root = config["brrtrouter_root"]
    wrapped = cli_name != "brrtrouter"

    port = "10351" if not wrapped else "10350"
    ns = f"kind-{pname}"
    root_env = "BRRTRouter_ROOT" if not wrapped else "BRRTROUTER_ROOT"
    venv_env = "BRRTRouter_VENV" if not wrapped else "BRRTROUTER_VENV"
    cli_venv_name = cli_name if not wrapped else pname

    _w(parts, f"# {pname} Development Environment")
    _w(parts, "# Auto-generated by photon — do not edit manually")
    _w(parts, "")
    _w(parts, f"allow_k8s_contexts(['{ns}'])")
    _w(parts, "")
    _w(parts, "update_settings(k8s_upsert_timeout_secs=60)")
    _w(parts, "")
    _w(parts, "docker_prune_settings(")
    _w(parts, "    disable=False,")
    _w(parts, "    max_age_mins=30,")
    _w(parts, "    keep_recent=1,")
    _w(parts, "    interval_hrs=1")
    _w(parts, ")")
    _w(parts, "")
    _w(parts, "config.define_string('tilt_port', args=False, usage='Port for Tilt web UI')")
    _w(parts, "cfg = config.parse()")
    _w(parts, f"tilt_port = cfg.get('tilt_port', '{port}')")
    _w(parts, "os.putenv('TILT_PORT', tilt_port)")
    _w(parts, "")

    # BRRTRouter root
    _w(parts, f"_brrtrouter_env = os.getenv('{root_env}', '').strip().rstrip('/')")
    _w(parts, f"brrtrouter_root = _brrtrouter_env if _brrtrouter_env else '{brrt_rel}'")
    _w(parts, "")

    # BRRTRouter venv
    _w(parts, f"_brrtrouter_venv_env = os.getenv('{venv_env}', '').strip().rstrip('/')")
    _w(parts, "_home = os.getenv('HOME', '') or os.getenv('USERPROFILE', '')")
    _w(
        parts,
        "brrtrouter_venv = _brrtrouter_venv_env if _brrtrouter_venv_env else (_home + '/.local/share/brrtrouter/venv' if _home else '.local/share/brrtrouter/venv')",
    )

    # Variable names for CLI binaries - use project name for wrapped projects
    if wrapped:
        _w(parts, f"{cli_name}_bin = '{cli_venv_name}/bin/{cli_name}' % brrtrouter_venv")
        _w(
            parts,
            f"{cli_name}_gen_bin = '{cli_venv_name}/target/debug/brrtrouter-gen' % brrtrouter_root",
        )
        # Keep alias for backward compat
        _w(parts, f"brrtrouter_bin = {cli_name}_bin")
        _w(parts, f"brrtrouter_gen_bin = {cli_name}_gen_bin")
    else:
        _w(parts, f"brrtrouter_bin = '{cli_venv_name}/bin/brrtrouter' % brrtrouter_venv")
        _w(
            parts,
            f"brrtrouter_gen_bin = '{cli_venv_name}/target/debug/brrtrouter-gen' % brrtrouter_root",
        )
    _w(parts, "")

    # Wrapped CLI binary — already handled above in header block

    # Tooling ignore
    _w(parts, "TOOLING_IGNORE = [")
    for p in [
        "**/*.pyc",
        "**/*.pyo",
        "**/__pycache__",
        "**/.pytest_cache",
        "**/.coverage",
        "**/.coverage.*",
        "**/htmlcov",
        "**/coverage.xml",
        "**/.ruff_cache",
        "**/.mypy_cache",
        "**/*.egg",
        "**/*.egg-info",
        "**/*.egg-info/**",
        "**/brrtrouter_tooling.egg-info/**",
        "**/*_tooling.egg-info/**",
        "**/build",
        "**/build/**",
        "**/.hypothesis",
        "**/.DS_Store",
    ]:
        _w(parts, f"    '{p}',")
    _w(parts, "]")
    _w(parts, "")

    # Tooling install
    _w(parts, "local_resource(")
    _w(parts, "    'build-tooling',")
    _w(parts, "    '''")
    _w(parts, "set -e")
    _w(parts, f'VENV="{brrt_venv}"')
    _w(parts, f'BRRROOT="{brrt_root}"')
    _w(parts, 'test -d "$VENV" || python3 -m venv "$VENV"')
    _w(parts, '"$VENV/bin/pip" install -U pip')
    _w(parts, '"$VENV/bin/pip" install -e "$BRRROOT/tooling[dev]"')
    _w(parts, "cd tooling && \"$VENV/bin/pip\" install -e '[dev]'")
    _w(parts, "    '''")
    _w(parts, "    deps=[")
    _w(parts, "        './tooling/pyproject.toml',")
    _w(parts, f"        '{brrt_rel}/tooling/pyproject.toml' %% brrtrouter_root,")
    _w(parts, "    ],")
    _w(parts, "    ignore=TOOLING_IGNORE,")
    _w(parts, "    labels=['tooling'],")
    _w(parts, "    allow_parallel=True,")
    _w(parts, ")")
    _w(parts, "")

    # K8s namespaces
    _w(parts, "k8s_yaml(kustomize('./k8s/data'))")
    _w(parts, "k8s_yaml(kustomize('./k8s/microservices'))")
    _w(parts, "")


