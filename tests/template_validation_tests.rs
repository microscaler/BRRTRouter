use brrtrouter::generator::{TemplateType, TemplateValidator, ValidationConfig, ValidationError};
use std::fs;
use tempfile::TempDir;

fn create_test_template(dir: &std::path::Path, name: &str, content: &str) -> std::path::PathBuf {
    let path = dir.join(name);
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).unwrap();
    }
    fs::write(&path, content).unwrap();
    path
}

#[test]
fn test_template_validation_successful_rust_template() {
    let temp_dir = TempDir::new().unwrap();
    let template_dir = temp_dir.path().join("templates");
    fs::create_dir_all(&template_dir).unwrap();

    let rust_template = r#"
// ⚠️  AUTO-GENERATED CODE - DO NOT EDIT
// This file is generated by BRRTRouter from OpenAPI specifications.
// Manual changes will be overwritten during code generation.

use serde::{Deserialize, Serialize};
use crate::brrtrouter::typed::TypedHandlerRequest;

/// {{ handler_name }} handler implementation
#[derive(Debug)]
pub struct {{ struct_name }} {
    pub name: String,
    pub value: {{ field_type }},
}

impl Handler for {{ struct_name }} {
    type Request = Request;
    type Response = Response;

    fn handle(&self, req: TypedHandlerRequest<Self::Request>) -> anyhow::Result<Self::Response> {
        {% for field in response_fields %}
        let {{ field.name }} = {{ field.value }};
        {% endfor %}
        
        Ok(Response {
            {% for field in response_fields %}
            {{ field.name }},
            {% endfor %}
        })
    }
}
"#;

    create_test_template(&template_dir, "handler.rs.txt", rust_template);

    let config = ValidationConfig {
        template_dir: template_dir.clone(),
        ..Default::default()
    };
    let mut validator = TemplateValidator::new(config);

    let template_path = template_dir.join("handler.rs.txt");
    let result = validator.validate_template(&template_path);

    assert!(result.is_ok(), "Rust template validation should succeed");

    let metadata = result.unwrap();
    assert_eq!(metadata.template_type, TemplateType::Rust);
    assert!(metadata
        .required_variables
        .contains(&"handler_name".to_string()));
    assert!(metadata
        .required_variables
        .contains(&"struct_name".to_string()));
    assert!(metadata
        .required_variables
        .contains(&"field_type".to_string()));
    assert!(metadata
        .required_variables
        .contains(&"response_fields".to_string()));
}

