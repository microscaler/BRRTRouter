#[test]
fn test_validation_error_display() {
    let errors = vec![
        ValidationError::TemplateNotFound(std::path::PathBuf::from("missing.rs")),
        ValidationError::SyntaxError("Bad syntax".to_string()),
        ValidationError::MissingVariables(vec!["var1".to_string(), "var2".to_string()]),
        ValidationError::CompilationError("Failed to compile".to_string()),
        ValidationError::GeneratedCodeError("Code error".to_string()),
        ValidationError::InvalidOutput("Invalid output".to_string()),
    ];

    for error in errors {
        let display = format!("{}", error);
        assert!(!display.is_empty(), "Error display should not be empty");

        // Check that error type is reflected in the message
        match error {
            ValidationError::TemplateNotFound(_) => assert!(display.contains("not found")),
            ValidationError::SyntaxError(_) => assert!(display.contains("syntax error")),
            ValidationError::MissingVariables(_) => assert!(display.contains("Missing")),
            ValidationError::CompilationError(_) => assert!(display.contains("compilation")),
            ValidationError::GeneratedCodeError(_) => assert!(display.contains("Generated code")),
            ValidationError::InvalidOutput(_) => assert!(display.contains("Invalid")),
        }
    }
}
