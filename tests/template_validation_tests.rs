#[test]
fn test_template_validation_all_templates() {
    let temp_dir = TempDir::new().unwrap();
    let template_dir = temp_dir.path().join("templates");
    fs::create_dir_all(&template_dir).unwrap();

    // Create multiple valid templates
    let rust_template = r#"
// ⚠️  AUTO-GENERATED CODE - DO NOT EDIT
use serde::Serialize;
pub struct {{ name }} {}
"#;

    let toml_template = r#"
# ⚠️  AUTO-GENERATED CODE - DO NOT EDIT
[package]
name = "{{ name }}"
"#;

    let html_template = r#"
<!-- ⚠️  AUTO-GENERATED CODE - DO NOT EDIT -->
<!DOCTYPE html>
<html><head><title>{{ title }}</title></head></html>
"#;

    create_test_template(&template_dir, "handler.rs.txt", rust_template);
    create_test_template(&template_dir, "Cargo.toml.txt", toml_template);
    create_test_template(&template_dir, "index.html.txt", html_template);

    // Create one invalid template
    let bad_template = r#"
use serde::Serialize;
pub struct {{ unclosed
"#;
    create_test_template(&template_dir, "bad.rs.txt", bad_template);

    let config = ValidationConfig {
        template_dir: template_dir.clone(),
        ..Default::default()
    };
    let mut validator = TemplateValidator::new(config);

    let report = validator.validate_all().unwrap();

    assert_eq!(report.total_processed, 4);
    assert_eq!(report.success_count(), 3);
    assert_eq!(report.failure_count(), 1);
    assert!(!report.all_passed());
    assert_eq!(report.success_rate(), 75.0);

    // Check that the report summary is correct
    let summary = report.summary();
    assert!(summary.contains("3/4 passed"));
    assert!(summary.contains("75.0%"));
}

#[test]
fn test_template_validation_subdirectories() {
    let temp_dir = TempDir::new().unwrap();
    let template_dir = temp_dir.path().join("templates");
    fs::create_dir_all(&template_dir).unwrap();

    // Create templates in subdirectories
    let base_dir = template_dir.join("base");
    let components_dir = template_dir.join("components");
    fs::create_dir_all(&base_dir).unwrap();
    fs::create_dir_all(&components_dir).unwrap();

    let base_template = r#"
// ⚠️  AUTO-GENERATED CODE - DO NOT EDIT
use serde::Serialize;
pub struct {{ name }} {}
"#;

    let component_template = r#"
// ⚠️  AUTO-GENERATED CODE - DO NOT EDIT
impl TryFrom<HandlerRequest> for {{ request_type }} {
    type Error = anyhow::Error;
    fn try_from(req: HandlerRequest) -> Result<Self, Self::Error> {
        {% for param in parameters %}
        let {{ param.name }} = req.{{ param.location }}_params.get("{{ param.name }}");
        {% endfor %}
        Ok(Self { })
    }
}
"#;

    create_test_template(&base_dir, "handler_base.rs.txt", base_template);
    create_test_template(
        &components_dir,
        "parameter_extraction.rs.txt",
        component_template,
    );

    let config = ValidationConfig {
        template_dir: template_dir.clone(),
        ..Default::default()
    };
    let mut validator = TemplateValidator::new(config);

    let report = validator.validate_all().unwrap();

    assert_eq!(report.total_processed, 2);
    assert_eq!(report.success_count(), 2);
    assert!(report.all_passed());
}

#[test]
fn test_template_validation_configuration() {
    let temp_dir = TempDir::new().unwrap();
    let template_dir = temp_dir.path().join("templates");
    fs::create_dir_all(&template_dir).unwrap();

    let large_template = "a".repeat(2048); // 2KB template
    create_test_template(&template_dir, "large.rs.txt", &large_template);

    // Test with size limit
    let config = ValidationConfig {
        template_dir: template_dir.clone(),
        max_template_size: 1024, // 1KB limit
        ..Default::default()
    };
    let mut validator = TemplateValidator::new(config);

    let template_path = template_dir.join("large.rs.txt");
    let result = validator.validate_template(&template_path);

    assert!(
        result.is_err(),
        "Template exceeding size limit should fail validation"
    );

    if let Err(ValidationError::InvalidOutput(msg)) = result {
        assert!(msg.contains("exceeds maximum"));
    } else {
        panic!(
            "Expected InvalidOutput error about size limit, got: {:?}",
            result
        );
    }
}

